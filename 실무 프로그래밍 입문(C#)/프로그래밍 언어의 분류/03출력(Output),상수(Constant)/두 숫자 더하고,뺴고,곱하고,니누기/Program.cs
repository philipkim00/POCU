using System;

namespace 두_숫자_더하고_뺴고_곱하고_니누기
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //두 숫자 더하기
            Console.Write("10 + 20 =");
            Console.WriteLine(10 + 20);

            //두 숫자 뺴기
            Console.Write("10 - 20 =");
            Console.WriteLine(10 - 20);

            // 두 숫자 곱하기
            Console.Write("10 * 20 =");
            Console.WriteLine(10 * 20);

            // 두 숫자 나누기
            Console.Write("10 / 20 =");
            Console.WriteLine(10 / 20);
        }
    }
}
